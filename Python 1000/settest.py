pest = {'p', 'e', 's', 't'}
rest = {'r', 'e', 's', 't'}
best = {'b', 'e', 's', 't'}

print('pest | rest | best : ', pest | rest | best)
print('pest & rest & best : ', pest & rest & best)
print('pest - rest - best : ', pest - rest - best)
print('rest - pest - best : ', rest - pest - best)
print('best - rest - pest : ', best - rest - pest)
print('pest ^ rest ^ best : ', pest ^ rest ^ best)
